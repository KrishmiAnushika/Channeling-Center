    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String speId = txtSpecialityId.getText();
        String speName = txtSpecialityName.getText();
        SpecialityDTO specialityDTO = new SpecialityDTO(speId, speName);
        try {
            boolean add = specialityController.add(specialityDTO);
            if (add) {
                JOptionPane.showMessageDialog(this, "SUCCESS");
               }else{
                JOptionPane.showMessageDialog(this, "SOMETHING WENT WRONG");
            }
        } catch (Exception ex) {
            Logger.getLogger(Speciality.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed


