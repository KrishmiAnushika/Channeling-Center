    private void autoGenId() {
        try {
            String newID = AutoGenId.getNewID("speciality", "speId", "SPE");
            txtSpecialityId.setText(newID);
        } catch (SQLException ex) {
            Logger.getLogger(Speciality.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Speciality.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
