    private void txtTotalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtTotalMouseClicked
        double docCharge = 0;
        double otherCharges = 0;
        double discount = 0;
        if (!txtDocCharge.getText().isEmpty()) {
            docCharge = Double.parseDouble(txtDocCharge.getText());
        } else {
            docCharge = 0;
        }
        if (!txtOtherCharge.getText().isEmpty()) {
            otherCharges = Double.parseDouble(txtOtherCharge.getText());
        } else {
            otherCharges = 0;
        }
        if (!txtDiscount.getText().isEmpty()) {
            discount = Double.parseDouble(txtDiscount.getText());
        } else {
            discount = 0;
        }
        double total = (docCharge + otherCharges) - (docCharge + otherCharges) / 100 * discount;
        txtTotal.setText(Double.toString(total));
    }//GEN-LAST:event_txtTotalMouseClicked

