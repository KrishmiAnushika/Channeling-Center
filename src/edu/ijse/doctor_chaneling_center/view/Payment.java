    private void loadDetails() {
        String appId = txtAppointmentId.getText();
        try {
            AppointmentDTO search = appointmentController.search(appId);
            if (search != null) {
                String patId = search.getPatId();
                String docId = search.getDocId();
                PatientDTO search1 = patientController.search(patId);
                txtPatientNic.setText(search1.getPatId());
                txtPatientName.setText(search1.getPatName());
                txtPatientAddress.setText(search1.getPatAddress());
                txtPatientTele.setText(Integer.toString(search1.getPatTele()));
                txtAge.setText(Integer.toString(search1.getAge()));

                DoctorDTO search2 = controller.search(docId);
                txtDocName.setText(search2.getDocName());
                txtHospital.setText(search2.getHospital());
                txtDocTele.setText(Integer.toString(search2.getDocTele()));
                txtDocCharge.setText(Double.toString(search2.getDocCharge()));

                String speId = search2.getSpecialityId();
                SpecialityDTO search3 = specialityController.search(speId);
                txtSpeciality.setText(search3.getSpename());
            }
        } catch (Exception ex) {
            Logger.getLogger(Payment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void addPaymentDetails() {
        String payId = txtPaymentId.getText();
        Reception_DashBoard reception_DashBoard = new Reception_DashBoard();
        String date = reception_DashBoard.lblDateMain.getText();
        String time = reception_DashBoard.lblTimeMain.getText();
        double total = Double.parseDouble(txtTotal.getText());
        double discount = Double.parseDouble(txtDiscount.getText());
        double extraCharge = Double.parseDouble(txtOtherCharge.getText());
        String appId = txtAppointmentId.getText();

        PaymentDTO paymentDTO = new PaymentDTO(payId, date, time, total, discount, extraCharge, appId);
        try {
            boolean add = paymentController.add(paymentDTO);

            if (add) {
                JOptionPane.showMessageDialog(this, "SUCCESS");
                //sendPrint();
            } else {
                JOptionPane.showMessageDialog(this, "SOMETHING WENT WRONG...!");
            }
        } catch (Exception ex) {
            Logger.getLogger(Payment.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

